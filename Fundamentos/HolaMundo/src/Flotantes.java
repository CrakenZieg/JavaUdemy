
public class Flotantes {
    
    public static void main(String args[]) {
        
        //primitivos del tipo flotante: float y double
        
        float numeroFloat = 100;
        System.out.println("numeroFloat = " + numeroFloat);
        System.out.println("Rango del Float: " + Float.MIN_VALUE + " a " + Float.MAX_VALUE);
        
        double numeroDouble = 10.05;
        System.out.println("Rango del Double: " + Double.MIN_VALUE + " a " + Double.MAX_VALUE);
        
    }
    
}
